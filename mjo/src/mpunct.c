static void
  _mjo_utf8_is_punctuation(int *const o_is_punct,
                           struct mjo_utf8 const *const i_peek)
{
  size_t l_slots;

  l_slots
    = (sizeof(g_utf8_punctuation_table) / sizeof(g_utf8_punctuation_table[0]));

  mjo_search_binary(
    o_is_punct,
    0,
    (void const *)g_utf8_punctuation_table,
    sizeof(struct mjo_utf8),
    l_slots,
    (void (*)(int *const, void const *, void const *))mjo_utf8_compare,
    (void const *const *)i_peek);

  return;
}

extern int
  mjo_utf8_is_punctuation(int *const o_is_punct,
                          struct mjo_utf8 const *const i_peek)
{
  int l_rc;

  l_rc = MJO_FAIL;

  do
    {
      if (0 == o_is_punct)
        {
          errno = EINVAL;
          break;
        }

      l_rc = MJO_OK;

      *o_is_punct = 0;

      if (0 == i_peek)
        {
          break;
        }

      if (0 == i_peek->m_legal)
        {
          break;
        }

      _mjo_utf8_is_punctuation(o_is_punct, i_peek);
    }
  while (0);

  return l_rc;
}
